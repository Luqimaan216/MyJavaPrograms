
public class ArithmeticOperator {
	public static void main(String [] args) {
		int a;
		int b;
		int c;
		a = 10;
		b = 6;
		c = 2;
		
		System.out.println("a = 10, b = 6, c = 2");
		System.out.println("a + b * c = "+(a+b*c));
		System.out.println("39 / a + b = "+(39/a+b));
		System.out.println("(a + b)*c = "+((a+b)*c));
		System.out.println("3*(5 + b) = "+((a+b)*c));
		System.out.println("a - 11*c / b = "+(a-11*c/b));
		System.out.println("a * b + b / c - 1 = "+(a*b+b/c-1));
		System.out.println("27 % 3 + c = "+(27%3+c));
		System.out.println("2 * a % - 3 = "+(2*a%-3));
	}

}
